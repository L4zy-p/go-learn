	// num กับ num2 ค่าจะไม่เหมือนกันทั้งที่ค่าเท่ากัน
	var num float32 = 3.1456545456456456465454654546545456455465454654564564
	fmt.Println(num) // 3.1456544

	var num2 float64 = 3.1456545456456456465454654546545456455465454654564564
	fmt.Println(num2) // 3.1456545456456455
