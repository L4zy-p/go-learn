package main

import "fmt"

func main() {
	var num int8 = 14 + 10/2 - 2
	fmt.Println(num) // 19
	// จากตัวอย่างข้างบน ถ้ามี operator หลายตัวจะไปคิดตัวที่มีกำลังมากก่อนก็คือ หาร และแล้วค่อยไป บวก
	// โดยกำลังว่าจะตัวไหนก่อน ถ้ามี  + - * / %
	// จะทำ * / % (level เท่ากัน) ก่อน จึงจะมาทำ + - (level เท่ากัน)

	// สมมติว่าเราอยากให้ทำในส่วนของ 14 + 10 ก่อน เราต้องใส่ () ให้คือ (14 + 10)
}
